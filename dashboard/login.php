    header("location:dashboard/train-m/dashboard.php");
}
if(isset($_POST["login"])){
    if(empty($_POST["username"]) && empty($_POST["password"])){
        echo '<script>alert("Both Fields are Required !")</script>';
    }
    else{
        $username = mysqli_real_escape_string($dbconnect, $_POST["username"]);
        $password = mysqli_real_escape_string($dbconnect, $_POST["password"]);
        $password = md5($password);
        $query = "SELECT * FROM train_sys.users WHERE user_name = '$username' AND password = '$password'";
        $result = mysqli_query($dbconnect, $query);
        if(mysqli_num_rows($result) > 0){
            $_SESSION['username'] = $username;
            header("location:train-m/dashboard.php");
        }
        else{
            echo '<script>alert("Wrong User Info !")</script>';
        }
    }
